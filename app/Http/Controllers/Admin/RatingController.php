    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $doctor = Auth::user();
        $doctors = Doctor::where('id', $doctor->id)->with(['ratings' => function ($query) use ($doctor) {
            $query->where('doctor_rating.doctor_id', $doctor->id);
        }])->get();

        $averageRating = $doctors->flatMap(function ($doctor) {
            return $doctor->ratings;
        })->avg('stars');;




        return view('admin.doctors.statistics', compact('doctors', 'averageRating'));
    }

